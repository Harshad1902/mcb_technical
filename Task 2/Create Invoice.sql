CREATE TABLE INVOICE (
    INVOICE_ID          NUMBER          GENERATED BY DEFAULT ON NULL AS IDENTITY,
    INVOICE_ORDER_ID    NUMBER          NOT NULL,
    INVOICE_REFERENCE   VARCHAR2(50),
    INVOICE_DATE        DATE,
    CONSTRAINT INVOICE_PK PRIMARY KEY (INVOICE_ID),
    CONSTRAINT INVOICE_FK FOREIGN KEY (INVOICE_ORDER_ID) REFERENCES ORDERS(ORDER_ID)
);
/

