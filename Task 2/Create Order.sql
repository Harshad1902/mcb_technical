CREATE TABLE ORDERS (
    ORDER_ID                NUMBER          GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ORDER_SUPPLIER_ID       NUMBER          NOT NULL,
    ORDER_REFERENCE         VARCHAR2(50)    NOT NULL,
    ORDER_DATE              DATE            NOT NULL,
    ORDER_TOTAL_AMOUNT      DECIMAL(38,2),
    ORDER_STATUS            VARCHAR2(25)    NOT NULL,
    ORDER_DESCIRIPTION      VARCHAR(200),    
    CONSTRAINT ORDER_PK PRIMARY KEY (ORDER_ID),
    CONSTRAINT ORDER_SUPPLIER_FK FOREIGN KEY (ORDER_SUPPLIER_ID) REFERENCES SUPPLIER(SUPPLIER_ID)
);
