CREATE TABLE Order_Details (
    ORDER_DETAILS_ID        NUMBER          GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ORDER_DETAILS_ORDER_ID  NUMBER          NOT NULL,
    ORDER_LINE_AMOUNT       DECIMAL(38,2),
    ORDER_STATUS            VARCHAR2(25)    NOT NULL,
    ORDER_REFERENCE         VARCHAR(20)        NOT NULL,
    ORDER_DESCIRIPTION      VARCHAR(200),    
    CONSTRAINT ORDER_DETAILS_PK PRIMARY KEY (ORDER_DETAILS_ID),
    CONSTRAINT ORDER_DETAILS_FK FOREIGN KEY (ORDER_DETAILS_ORDER_ID) REFERENCES ORDERS(ORDER_ID)
);
/

